
public class Code04_16 {
	public static void main(String[] args) {
		String str = "Java";
		
		System.out.println(str.charAt(0));//str의 0번째 문자 J 출력
		System.out.println(str.charAt(1));//str의 1번째 문자 a 출력
		System.out.println(str.charAt(2));//str의 2번째 문자 v 출력
		System.out.println(str.charAt(3));//str의 3번째 문자 a 출력
//		System.out.println(str.charAt(4));->오류 발생, 4번째 문자가 존재하지 않기 때문에
	}
}
