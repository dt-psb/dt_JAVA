
public class Code05_03 {
	public static void main(String[] args) {
		int num =200; //변수 선언 및 초기값 설정
		
		if (num<100)  //만약  num(200)가 100보다 작으면 아래 코드 실행
			System.out.println("100보다 작군요."); //거짓이므로 출력X
		else //만약 if문을 실행하지 않으면 아래 코드 실행
			System.out.println("100보다 크군요."); //if문 실행되지 않았으므로 출력O
		
	}
}
