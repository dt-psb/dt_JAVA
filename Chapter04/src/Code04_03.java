
public class Code04_03 {
	public static void main(String[] args) {
		int n1 =1; //정수형 변수1
		int n2 =2; //정수형 변수2
		
		System.out.println(n1+n2); //결과 정수
		System.out.println(n1-n2);
		System.out.println(n1*n2);
		System.out.println(n1/n2);
		// 출력 결과->정수
		
		
		int a1 =1; //정수형 변수 
		double a2 =2; //실수형 변수
		
		System.out.println(a1+a2);
		System.out.println(a1-a2);
		System.out.println(a1*a2);
		System.out.println(a1/a2);
		//출력 결과 -> 실수
		
		// 실수 vs 정수 -> 실수 우선
	}
}
